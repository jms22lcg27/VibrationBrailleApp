Braille(String ttext) {
  List<String> Sentence = [];
  var Word = [];
  List<int> VibrationBraille = [];
  var ch1, ch2, ch3;
  // 초성 리스트
  List<String> CHOSUNG_LIST = [
    'ㄱ',
    'ㄲ',
    'ㄴ',
    'ㄷ',
    'ㄸ',
    'ㄹ',
    'ㅁ',
    'ㅂ',
    'ㅃ',
    'ㅅ',
    'ㅆ',
    'ㅇ',
    'ㅈ',
    'ㅉ',
    'ㅊ',
    'ㅋ',
    'ㅌ',
    'ㅍ',
    'ㅎ'
  ];
  // 중성 리스트
  List<String> JUNGSUNG_LIST = [
    'ㅏ',
    'ㅐ',
    'ㅑ',
    'ㅒ',
    'ㅓ',
    'ㅔ',
    'ㅕ',
    'ㅖ',
    'ㅗ',
    'ㅘ',
    'ㅙ',
    'ㅚ',
    'ㅛ',
    'ㅜ',
    'ㅝ',
    'ㅞ',
    'ㅟ',
    'ㅠ',
    'ㅡ',
    'ㅢ',
    'ㅣ'
  ];
  // 종성 리스트
  List<String> JONGSUNG_LIST = [
    ' ',
    'ㄱ',
    'ㄲ',
    'ㄳ',
    'ㄴ',
    'ㄵ',
    'ㄶ',
    'ㄷ',
    'ㄹ',
    'ㄺ',
    'ㄻ',
    'ㄼ',
    'ㄽ',
    'ㄾ',
    'ㄿ',
    'ㅀ',
    'ㅁ',
    'ㅂ',
    'ㅄ',
    'ㅅ',
    'ㅆ',
    'ㅇ',
    'ㅈ',
    'ㅊ',
    'ㅋ',
    'ㅌ',
    'ㅍ',
    'ㅎ'
  ];

  //추출된 텍스트 분리해 점자 리스트 찾기
  Sentence = ttext.split('');
  for (int i = 0; i < ttext.length; i++) {
    if ((44032 <= ttext[i].runes.first) && (ttext[i].runes.first <= 55203) ||
        (12593 <= ttext[i].runes.first) && (ttext[i].runes.first <= 12643)) {
      // 한글 이라면
      ch1 = (ttext[i].runes.first - 44032) ~/ 588; //초성
      ch2 = (ttext[i].runes.first - 44032 - (588 * ch1)) ~/ 28; //중성
      ch3 = ttext[i].runes.first - 44032 - (588 * ch1) - 28 * ch2; //종성
      Word.add(CHOSUNG[CHOSUNG_LIST[ch1]]);
      Word.add(JUNGSUNG[JUNGSUNG_LIST[ch2]]);
      Word.add(JONGSUNG[JONGSUNG_LIST[ch3]]);
    } else {
      //알파벳 or 기호
      Word.add(ALPHABET[ttext[i]]);
    }
  }

  //번역된 점자리스트에 대한 진동시간 리스트 만들기
  for (var i in Word) {
    for (var j in i) {
      VibrationBraille.add(800); //다른 점자로 넘어갈때 0.8초 대기
      int count = 0;
      for (var f in j) {
        count++;
        if (f == 1) {
          f = 500; //점자 표시된 곳 0.5초 진동
        } else {
          f = 200; //점자 표시 되지않은곳 0.2초 대기
        }
        VibrationBraille.add(f);
        if (count < 6) {
          VibrationBraille.add(100); //점자 사이마다 0.1초 대기
        }
      }
    }
  }
  return (VibrationBraille); //진동시간 리스트 반환
}
